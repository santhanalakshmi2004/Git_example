const mongodb = require("mongoose");
const userschema = mongodb.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
const user = mongodb.model("jeeva", userschema);
module.exports = user;
